# One of the main forces in driving scientific advancement
# is for findings to be able to be reproduced.  In terms
# of statistical analysis, this traditionally involved carefully
# keeping track of the point-and-click operations to produce a plot
# and using a package such as Excel to perform the analysis.  We will
# discuss many of the issues this process has created and we will then
# talk about the shift to make research “reproducible”, i.e, data analyses
# is published with their data and subsequent code.  Lastly, we will
# examine how to use the rmarkdown package in R to manage projects
# in a reproducible way to better support the validity of scientific claims.